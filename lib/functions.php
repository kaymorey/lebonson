function slug($str) {
	return strtolower(preg_replace(array('/[^a-zA-Z0-9 -]/', '/[ -]+/', '/^-|-$/'), 
		array('', '-', ''), remove_accent($str))); 
}
function sendRegistrationMail($mail, $key) {
	if (!preg_match("#^[a-z0-9._-]+@(hotmail|live|msn).[a-z]{2,4}$#", $mail)) {
    	$newline = "\r\n";
	}
	else {
    	$newline = "\n";
	}

	// Contenu du message
	$messageContent = 
	"<html>
	<head>
	</head>
	<body>
	<p>
		Cher client,<br /><br />	
		Nous vous souhaitons la bienvenue sur Le bon son.<br />
		Les identifiants de votre compte viennent d'être créés. Toufefois afin de valider votre inscription veuillez activer votre compte en cliquant sur le lien suivant.<br />
		<a href=\"http://katiamoreira.alwaysdata.net/lebonson/registration.php?valid_register=$mail&key=$key\">http://katiamoreira.alwaysdata.net/lebonson/registration.php?valid_register=$mail&key=$key</a>
		<br /><br />
		A bientôt sur <a href=\"http://katiamoreira.alwaysdata.net/lebonson\">Le bon son</a>.
	<hr />
	<p>
		<em>Ceci est un mail automatique. Merci de ne pas y répondre.</em>
	</p>
	</body>
	</html>";
	// Création du boundary
	$boundary = "-----=".md5(rand());

	$subject = "Activation de votre compte sur Le bon son.";

	// En-têtes
	$header = "From: \"Le bon son\"<noreply-lebonson@alwaysdata.net>".$newline;
	$header .= "Reply-to: \"Le bon son\" <noreply-lebonson@alwaysdata.net>".$newline;
	$header .= "MIME-Version: 1.0".$newline;
	$header .= "Content-Type: multipart/alternative;".$newline." boundary=\"$boundary\"".$newline;

	// Ouverture boundary
	$message = $newline."--".$boundary.$newline;
	$message .= "Content-Type: text/html; charset=\"utf-8\"".$newline;
	$message .= "Content-Transfer-Encoding: 8bit".$newline;
	$message .= $newline.$messageContent.$newline;
	// Fermeture boundary
	$message .= $newline."--".$boundary."--".$newline;

	// Envoi mail
	mail($mail, $subject, $message, $header);
}
function generateSalt() {
	$salt = "$2y$14$";
	for ($i = 0; $i < 22; $i++) {
    	$salt .= substr("./ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789", mt_rand(0, 63), 1);
	}

	return $salt;
}


